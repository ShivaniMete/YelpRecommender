package yelprecommender.model;

//lasvegastrends
public class Trends {
	private String city;
	private String zip;
	private String trendingCategory;
	private String trendingBusiness;
	
	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getTrendingCategory() {
		return trendingCategory;
	}

	public void setTrendingCategory(String trendingCategory) {
		this.trendingCategory = trendingCategory;
	}

	public String getTrendingBusiness() {
		return trendingBusiness;
	}

	public void setTrendingBusiness(String trendingBusiness) {
		this.trendingBusiness = trendingBusiness;
	}
	
}
